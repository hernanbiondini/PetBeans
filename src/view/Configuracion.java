    public void conf() {
        try {
            Properties p = new Properties();
            p.load(new FileInputStream("conf.ini"));
            this.setUsuario(ver.desencriptar(p.getProperty("Usuario"),CLAVE));
            this.setPassword(ver.desencriptar(p.getProperty("Password"),CLAVE));
            this.setBase(ver.desencriptar(p.getProperty("Base"),CLAVE));
            this.setHost(ver.desencriptar(p.getProperty("Host"),CLAVE));
            this.setPuerto(ver.desencriptar(p.getProperty("Puerto"),CLAVE));
            this.setDriver(ver.desencriptar(p.getProperty("Driver"),CLAVE));
            this.setRuta(ver.desencriptar(p.getProperty("Ruta"),CLAVE));
            this.setPath(ver.desencriptar(p.getProperty("Pathmysqldump"),CLAVE));
            this.setBackup(ver.desencriptar(p.getProperty("Backup"),CLAVE));
            this.setCache(ver.desencriptar(p.getProperty("hibernate.cache.provider_class"),CLAVE));
            this.setStartegy(ver.desencriptar(p.getProperty("hibernate.hbm2ddl.auto"),CLAVE));

        } catch (Exception e) {
            System.out.println(e);
        }
    }

