    public void guardar() throws IOException {

        Properties p = new Properties();
        p.load(new FileInputStream("conf.ini"));
        p.setProperty("Usuario", ver.encriptar(this.jTextField1.getText(),CLAVE));
        p.setProperty("Password", ver.encriptar(this.jTextField2.getText(),CLAVE));
        p.setProperty("Base", ver.encriptar(this.jTextField3.getText(),CLAVE));
        p.setProperty("Host", ver.encriptar(this.jTextField5.getText(),CLAVE));
        p.setProperty("Puerto", ver.encriptar(this.jTextField4.getText(),CLAVE));
        p.setProperty("Driver", ver.encriptar(this.jTextField6.getText(),CLAVE));
        p.setProperty("Ruta", ver.encriptar(this.jTextField7.getText(),CLAVE));
        p.setProperty("Pathmysqldump", ver.encriptar(this.jTextField8.getText(),CLAVE));
        p.setProperty("Backup", ver.encriptar(this.jTextField9.getText(),CLAVE));
        p.setProperty("hibernate.cache.provider_class", ver.encriptar(this.jTextField10.getText(),CLAVE));
        p.setProperty("hibernate.hbm2ddl.auto", ver.encriptar(this.jTextField11.getText(),CLAVE));

        FileOutputStream out = new FileOutputStream("conf.ini");
        p.save(out, "/* properties updated */");

    }
    
