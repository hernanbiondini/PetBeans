    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        if (jTextField1.getText().equalsIgnoreCase("")) {
            for (Iterator iterador = mails.listIterator(); iterador.hasNext();) {
                jTextField1.setText(jTextField1.getText() + iterador.next() + ";");
            }
            jButton3.setText("Deseleccionar todos");
        } else {
            jTextField1.setText("");
            jButton3.setText("Seleccionar todos");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

