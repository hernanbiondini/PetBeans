    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jTextField1.getText().trim().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(this, "Por favor ingrese al menos un destinatario", "Destinatario", JOptionPane.ERROR_MESSAGE);
        } else {

            util.Mail m = new util.Mail();
            if (m.hayConexion()) {
                try {
                    m.obtenerSesion();
                } catch (AddressException ex) {
                    Logger.getLogger(Mail.class.getName()).log(Level.SEVERE, null, ex);
                } catch (MessagingException ex) {
                    Logger.getLogger(Mail.class.getName()).log(Level.SEVERE, null, ex);
                }
                try {
                    m.crearMensaje(jTextField1.getText().trim(), jTextField2.getText().trim(), jTextArea1.getText().trim());
                } catch (AddressException ex) {
                    Logger.getLogger(Mail.class.getName()).log(Level.SEVERE, null, ex);
                } catch (MessagingException ex) {
                    Logger.getLogger(Mail.class.getName()).log(Level.SEVERE, null, ex);
                }
                try {
                    m.enviar();
                } catch (MessagingException ex) {
                    Logger.getLogger(Mail.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Por favor conectese a Internet", "Conexión", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

