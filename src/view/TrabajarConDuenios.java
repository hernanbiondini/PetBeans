    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

        this.propietario1 = new Propietario();
        int linea = jTable1.getSelectedRow();
        int iidd = (Integer) jTable1.getValueAt(linea, 0);
        String nom = jTable1.getValueAt(linea, 1).toString();
        String dir = jTable1.getValueAt(linea, 2).toString();
        String tel = jTable1.getValueAt(linea, 3).toString();
        String cel = jTable1.getValueAt(linea, 4).toString();
        String ema = jTable1.getValueAt(linea, 5).toString();
        propietario1.setIdPropietario(iidd);
        propietario1.setNombreDueño(nom);
        propietario1.setDireccion(dir);
        propietario1.setTelefonoFijo(tel);
        propietario1.setCelular(cel);
        propietario1.setEmail(ema);

    }//GEN-LAST:event_jTable1MouseClicked

