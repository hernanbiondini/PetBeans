    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if ((this.vacunaRefuerzo1.getIdVacuna() == 0) & (this.vacunaAnual1.getIdVacuna() == 0)) {
            JOptionPane.showMessageDialog(this, "Por favor seleccione una vacuna de alguna de las tabla", "Borrar", JOptionPane.INFORMATION_MESSAGE);
        } else {

            Object[] options = {"Si", "No", "Cancelar"};
            int n = JOptionPane.showOptionDialog(this, "Esta seguro que desea eliminar la vacuna seleccionada?", "Borrando...", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE, null, options, options[2]);
            if (JOptionPane.OK_OPTION == n) {
                try {
                    if (this.flag == "1") {
                        VacunaRefuerzoController.getInstance().borrar(vacunaRefuerzo1);
                        JOptionPane.showMessageDialog(this, "La vacuna con refuerzos se ha borrado exitosamente", "Borrar", JOptionPane.INFORMATION_MESSAGE);
                        refreshVacunaRefuerzo();
                    } else {
                        VacunaAnualController.getInstance().borrar(vacunaAnual1);
                        JOptionPane.showMessageDialog(this, "La vacuna anual se ha borrado exitosamente", "Borrar", JOptionPane.INFORMATION_MESSAGE);
                        refreshVacunaAnual();
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

